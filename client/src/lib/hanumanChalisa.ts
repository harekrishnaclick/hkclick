export function buildWordList(verses: ChalisaVerse[]): { word: string; verseId: string; lineIdx: number; wordIdx: number }[] {
  const result: { word: string; verseId: string; lineIdx: number; wordIdx: number }[] = [];
  for (const verse of verses) {
    for (let li = 0; li < verse.lines.length; li++) {
      for (let wi = 0; wi < verse.lines[li].length; wi++) {
        result.push({ word: verse.lines[li][wi], verseId: verse.id, lineIdx: li, wordIdx: wi });
      }
    }
  }
  return result;
}

